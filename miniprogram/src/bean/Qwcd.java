package bean;

public class Qwcd {
    public int id;
    public String year;
    public int completeSum;
    public int backSum;
    public int payment;
    public String name;

    public void setId(int id) {
        this.id = id;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public void setCompleteSum(int completeSum) {
        this.completeSum = completeSum;
    }

    public void setBackSum(int backSum) {
        this.backSum = backSum;
    }

    public void setPayment(int payment) {
        this.payment = payment;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {

        return id;
    }

    public String getYear() {
        return year;
    }

    public int getCompleteSum() {
        return completeSum;
    }

    public int getBackSum() {
        return backSum;
    }

    public int getPayment() {
        return payment;
    }

    public String getName() {
        return name;
    }
}
